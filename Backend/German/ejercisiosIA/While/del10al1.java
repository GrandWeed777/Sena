package ejercisiosIA.While;

public class del10al1 {
    public static void main(String[] args) {
        int contador = 10;

        while (contador > 0) {
            System.out.println(contador);
            contador--;
            
        }
    }
}
/**Realiza un programa en Java que muestre en pantalla los números del 10 al 1 en orden descendente.
El programa debe utilizar una variable contador que se vaya decrementando en cada iteración hasta llegar al número 1. */